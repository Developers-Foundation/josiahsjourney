Remembering Josiah always...

"Every good and perfect gift comes from above." James 1:17

Josiah "Jo-Jo" Scott Morash was born on July 28th, 2009 at St. Joseph's Hospital in London, Ontario.

Early into the pregnancy Josiah showed that he was going to be a very special baby.
At 18 weeks Josiah was diagnosed with a very rare genetic disorder known as Trisomy 13 or Patau's syndrome.

Because of the genetic disorder, Trisomy 13, Josiah was given only a 3% chance of surviving the pregnancy.
Beating incredible odds, Josiah grew close to full term and was born alive. Josiah was our Miracle Baby.

Due to the many complications of Trisomy 13, Josiah also died this very same day - his birth day.

Josiah's life was a Great Blessing to our family.  He brought more faith, hope and love to our lives than
we could have ever imagined.  Please join us as we continue with Josiah's legacy - a precious little red-headed
boy whose spirit was filled with courage.  Let us always remember Josiah as we reach out to help other families
in need of neonatal and paediatric intensive care.

